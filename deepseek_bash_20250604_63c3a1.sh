# Перейдіть у папку з вашим проектом
cd шлях_до_папки_з_проектом

# Ініціалізуйте Git репозиторій
git init

# Додайте файли
git add .

# Зробіть коміт
git commit -m "Initial commit with website files"

# Додайте віддалений репозиторій (замініть yourusername на ваш GitHub логін)
git remote add origin https://github.com/yourusername/kylv-luxeboxquets.git

# Завантажте зміни на GitHub
git push -u origin master